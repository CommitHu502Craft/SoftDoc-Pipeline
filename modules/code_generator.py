def generate_code_from_plan(plan_path: str, output_dir: str) -> List[str]:
    """
    便捷函数：从项目规划生成代码

    Args:
        plan_path: project_plan.json 路径
        output_dir: 输出目录

    Returns:
        生成的文件路径列表
    """
    generator = CodeGenerator(plan_path)
    return generator.generate_all(output_dir)
