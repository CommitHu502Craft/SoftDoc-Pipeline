    def _get_nodejs_package(self) -> str:
        """生成 Node.js package.json"""
        return json.dumps({
            "name": self.project_name.lower().replace(' ', '-'),
            "version": "1.0.0",
            "description": f"Generated project for {self.project_name}",
            "main": "index.js",
            "scripts": {
                "start": "node index.js",
                "dev": "nodemon index.js"
            },
            "dependencies": {
                "express": "^4.17.1",
                "mongoose": "^6.0.0",
                "cors": "^2.8.5",
                "dotenv": "^10.0.0"
            },
            "devDependencies": {
                "nodemon": "^2.0.12"
            }
        }, indent=2)

    def _generate_devops_files(self, output_dir: Path, language: str) -> List[str]:
        """
        生成 DevOps 配置文件 (Dockerfile, docker-compose.yml, .gitignore, .env.example, CI)
        让生成的项目更接近真实项目结构
        """
        files = []
        language = language.lower()

        try:
            # 1. 生成 Dockerfile
            dockerfile_content = self._get_dockerfile(language)
            dockerfile_path = output_dir / "Dockerfile"
            dockerfile_path.write_text(dockerfile_content, encoding='utf-8')
            files.append(str(dockerfile_path))

            # 2. 生成 docker-compose.yml
            compose_content = self._get_docker_compose(language)
            compose_path = output_dir / "docker-compose.yml"
            compose_path.write_text(compose_content, encoding='utf-8')
            files.append(str(compose_path))

            # 3. 生成 .gitignore
            gitignore_content = self._get_gitignore(language)
            gitignore_path = output_dir / ".gitignore"
            gitignore_path.write_text(gitignore_content, encoding='utf-8')
            files.append(str(gitignore_path))

            # 4. 生成 .env.example
            env_content = self._get_env_example(language)
            env_path = output_dir / ".env.example"
            env_path.write_text(env_content, encoding='utf-8')
            files.append(str(env_path))

            # 5. 生成 GitHub Actions CI
            workflows_dir = output_dir / ".github" / "workflows"
            workflows_dir.mkdir(parents=True, exist_ok=True)
            ci_content = self._get_github_ci(language)
            ci_path = workflows_dir / "ci.yml"
            ci_path.write_text(ci_content, encoding='utf-8')
            files.append(str(ci_path))

            logger.info(f"DevOps 配置文件生成完成，共 {len(files)} 个文件")

        except Exception as e:
            logger.error(f"生成 DevOps 配置文件失败: {e}")

        return files

