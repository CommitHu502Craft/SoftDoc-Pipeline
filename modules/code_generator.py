"""
代码生成器模块（基于 CodeTransformer V2.0）
不再依赖 Jinja2 模板，而是使用基于 LLM 的 AST 变换引擎
"""
import json
import logging
from pathlib import Path
from typing import List, Dict, Any
from datetime import datetime

from modules.code_transformer import CodeTransformer
from modules.dependency_validator import DependencyValidator

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


class CodeGenerator:
    """基于 CodeTransformer 的代码生成器 V2.0"""

    def __init__(self, plan_path: str):
        """
        初始化代码生成器

        Args:
            plan_path: project_plan.json 路径
        """
        self.plan_path = Path(plan_path)

        if not self.plan_path.exists():
            raise FileNotFoundError(f"项目规划文件不存在: {plan_path}")

        with open(self.plan_path, 'r', encoding='utf-8') as f:
            self.plan = json.load(f)

        self.project_name = self.plan.get("project_name", "未命名项目")

        # V2.0 核心组件
        # 1. 变换引擎：负责业务逻辑迁移和物理混淆
        self.transformer = CodeTransformer(self.plan)
        # 2. 依赖验证器：负责清理非法引包
        self.validator = DependencyValidator()

        logger.info(f"CodeGenerator V2.0 初始化: {self.project_name}")
        logger.info(f"目标语言: {self.transformer.target_language}")

    def generate_all(self, output_dir: str) -> List[str]:
        """
        执行完整代码生成流程
        """
        output_path = Path(output_dir)
        output_path.mkdir(parents=True, exist_ok=True)

        all_files = []

        try:
            logger.info("开始生成核心代码 (CodeTransformer)...")

            # 1. 调用变换引擎生成核心代码
            # 注意：transform_seed_to_project 内部会处理文件写入
            code_files = self.transformer.transform_seed_to_project(output_path)

            # 2. 依赖校验与清洗
            logger.info("开始执行依赖校验与清洗...")
            target_lang = self.transformer.target_language
            validated_files = []

            for file_path_str in code_files:
                path = Path(file_path_str)
                if not path.exists():
                    continue

                try:
                    content = path.read_text(encoding='utf-8')
                    # 清洗依赖
                    cleaned_content = self.validator.validate_and_clean(content, target_lang)

                    # 只有当内容发生变化时才写回，减少IO
                    if cleaned_content != content:
                        path.write_text(cleaned_content, encoding='utf-8')
                        logger.debug(f"已清洗文件: {path.name}")

                    validated_files.append(str(path))
                except Exception as e:
                    logger.error(f"校验文件失败 {path}: {e}")
                    # 即使校验失败也保留文件，但在列表里记录
                    validated_files.append(str(path))

            all_files.extend(validated_files)

            # 3. 生成配置文件 (requirements.txt, pom.xml 等)
            logger.info("生成项目配置文件...")
            config_files = self._generate_config_files(output_path, target_lang)
            all_files.extend(config_files)

            logger.info(f"项目生成完成，共 {len(all_files)} 个文件")
            return all_files

        except RuntimeError as e:
            # 质量闸门等硬错误应向上抛出，阻止后续文档/PDF继续执行。
            logger.error(f"代码生成流程硬失败: {e}", exc_info=True)
            raise
        except Exception as e:
            logger.error(f"代码生成流程发生严重错误: {e}", exc_info=True)
            # 在严重错误时返回已生成的文件列表，避免完全失败
            return all_files

    def _generate_config_files(self, output_dir: Path, language: str) -> List[str]:
        """根据目标语言生成对应的包管理配置"""
        files = []
        language = language.lower()

        try:
            if language == "python":
                # 生成 requirements.txt
                content = self._get_python_requirements()
                path = output_dir / "requirements.txt"
                path.write_text(content, encoding='utf-8')
                files.append(str(path))

            elif language == "java":
                # 生成 pom.xml
                content = self._get_java_pom()
                path = output_dir / "pom.xml"
                path.write_text(content, encoding='utf-8')
                files.append(str(path))

            elif language in ["node.js", "nodejs", "javascript"]:
                # 生成 package.json
                content = self._get_nodejs_package()
                path = output_dir / "package.json"
                path.write_text(content, encoding='utf-8')
                files.append(str(path))

            # 4. 生成 DevOps 配置文件
            devops_files = self._generate_devops_files(output_dir, language)
            files.extend(devops_files)

            # 5. 生成通用 README
            readme_path = output_dir / "README.md"
            readme_content = f"""# {self.project_name}

Generated by AI Software Generator V2.0

## Project Info
- **Project Name**: {self.project_name}
- **Language**: {language}
- **Generated At**: {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}

## Getting Started

### Prerequisites
See generated configuration files.

### Installation
Run the setup script or install dependencies manually.
"""
            readme_path.write_text(readme_content, encoding='utf-8')
            files.append(str(readme_path))

        except Exception as e:
            logger.error(f"生成配置文件失败: {e}")

        return files

